/**
 * Returns all observations for a given entity (institution) ID.
 */
export function getObservationsByEntity(entityId: string): MetricObservation[] {
  return metricObservations.filter((obs) => obs.entity_id === entityId);
}

/**
 * Returns all observations for a given metric ID.
 */
export function getObservationsByMetric(
  metricId: string
): MetricObservation[] {
  return metricObservations.filter((obs) => obs.metric_id === metricId);
}

/**
 * Returns all observations matching a given status (pending, validated,
 * approved, or rejected).
 */
export function getObservationsByStatus(
  status: string
): MetricObservation[] {
  return metricObservations.filter((obs) => obs.status === status);
}

/**
 * Returns observations that are not yet approved -- i.e., those with status
 * "pending" or "validated". Useful for governance review queues.
 */
export function getPendingObservations(): MetricObservation[] {
  return metricObservations.filter(
    (obs) => obs.status === "pending" || obs.status === "validated"
  );
}
